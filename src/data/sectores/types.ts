/**
 * Маркетплейс направлений (Промт B, 14.07.2026): общие типы карточек услуг.
 * Источник контента — согласованный каталог направления (Промт A);
 * цены и составы НЕ менять в коде без нового согласования.
 */
import type { Lang } from '../../config/site';

export interface ServiceTier {
  /** START / PRO / A MEDIDA (моно-метка тарифа) */
  plan: string;
  /** Крупная цифра карточки тарифа, напр. 'от 690' (валюта отдельно) или 'a medida' */
  price: string;
  /** Валюта; не задана — цена «a medida» без символа */
  cur?: string;
  /** Строка модели: '+ 39 €/мес — поддержка и обновление расценок' */
  note: string;
  /** Что входит в тариф (маркеры) */
  items: string[];
  featured?: boolean;
}

export interface ServiceProcessStep {
  title: string;
  text: string;
}

/** Схема «вход → ИИ/автоматизация → выход» (редизайн карточки, 14.07.2026). */
export interface ServiceFlow {
  /** Что подаём на вход, напр. 'Объект: помещения, объёмы, ваш шаблон Excel' */
  input: string;
  /** Что делает ИИ/автоматизация, напр. 'Claude for Excel + Skill с капитулос и расценками' */
  ai: string;
  /** Что получаем на выходе, напр. 'Смета с PEM/PEC и верным IVA за ~10 минут' */
  output: string;
}

/** Полный копирайт услуги на одном языке (паритет RU/ES/EN обязателен). */
export interface ServiceCopy {
  /** Название услуги (карточка + H1 под-страницы) */
  title: string;
  /** Оффер одной строкой (карточка + lead под-страницы) */
  offer: string;
  /** «Закрываем процесс: …» — конкретный БП/производственный процесс (карточка + под-страница) */
  closes?: string;
  /** Короткий бейдж срока/формата для карточки, напр. '~1 неделя' или 'под ключ' */
  badge?: string;
  /** Мини-схема «вход → ИИ → выход» для блока «Как это работает» */
  flow?: ServiceFlow;
  /** Боль клиента */
  pain: string;
  /** Что делаем */
  make: string;
  /** Результат (деньги/время) — выделяется */
  result: string;
  /** Что входит (scope) */
  includes: string[];
  /** Технологии — честно, без завышения */
  tech: string;
  /** Ключевое ограничение / дисклеймер (не завышаем) */
  limits: string;
  /** Цена «от» для карточки в сетке, напр. 'от 690 €' */
  priceFrom: string;
  /** Подпись модели для карточки, напр. '+ 39 €/мес · setup + сопровождение' */
  priceNote: string;
  tiers: ServiceTier[];
  /** Аудит → настройка → тест → запуск → сопровождение */
  process: ServiceProcessStep[];
  /** Срок, напр. 'Срок ~1 неделя' */
  timeline: string;
  /** SLA, напр. 'Ответ ≤ 1 раб. дня (Pro ≤ 4 ч)' */
  sla?: string;
  /** Как тестируем / сопровождаем / обучаем */
  quality: string;
  /** Подпись основной CTA, напр. 'Заказать генератор смет' */
  cta: string;
  seo: { title: string; description: string };
}

export interface SectorService {
  /** Стабильный id, напр. 'presupuestos' */
  id: string;
  /** Номер в каталоге направления (1–14) */
  num: number;
  /** A — услуги направления; B — сквозные (на каждом направлении) */
  block: 'A' | 'B';
  /** Слаг под-страницы по языкам (без слэшей) */
  slug: Record<Lang, string>;
  copy: Record<Lang, ServiceCopy>;
}
